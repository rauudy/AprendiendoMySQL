-- SELECT es una instrucción que se utiliza para seleccionar datos de una base de datos. Los datos devueltos se almacenan en una tabla de resultados, llamada conjunto de resultados.

-- Seleccionar todo de una tabla
SELECT * FROM users; 

-- Seleccionar columna específica de una tabla
SELECT name FROM users;

-- Seleccionar columnas específicas de una tabla
SELECT user_id, name FROM users;